// 数据_变量_内存
/**
 * 什么是数据？
 * 数据是存储在内存中代表特定信息的内容，本质上是0101...
 * 数据的特点：可传递、可运算
 * 一切皆数据
 * 内存中所有操作的目标都是数据
 * 算术运算、逻辑运算、赋值、运行函数
 *
 * 什么是内存？
 * 内存条通电后产生的可存储数据的空间(临时的)，硬盘中的数据的持久存储的
 * 内存产生和死亡：内存条(电路板)=>通电=>产生内存空间=>存储数据=>处理数据=>断电=>内存空间和数据都消失
 * 一块小内存的2个数据
 *  1. 内部存储的数据
 *  2. 地址值
 * 内存分类：
 *  栈内存：全局变量/局部变量
 *  堆内存：对本身在堆内存中，标识对象的变量在栈内存中
 *
 * 什么是变量？
 * 可变化的量，由变量名和变量值组成
 * 每个变量都对应一块小内存，变量名用来查找对应的内存，变量值就是内存中保存的数据。
 *
 * 内存，数据，变量三者之间的关系
 * 内存用来存储数据的空间
 * 变量是内存的标识
*/
